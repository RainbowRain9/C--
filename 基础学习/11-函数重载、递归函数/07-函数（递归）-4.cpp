/*
描述
编写递归函数将输入的整数逐个数字输出。如输入12345，输出1 2 3 4 5，
每个数字之间用空格隔开。


输入
一个正整数n，表示n组案例。

每组案例由一个正整数m构成。

输出
针对每组案例，将输入的整数逐个数字输出，每个数字之间用空格隔开，最后一位数字后面没有空格。

每组案例输出完都要换行。

样例输入
1

12345

样例输出
1 2 3 4 5
*/

#include <iostream>
using namespace std;

// 函数f：递归地输出整数的每个数字
void f(int m) {
    // 基础情况：如果m是一个单位数（小于10），直接输出
    if (m < 10) {
        cout << m;
    } else {
        // 递归情况：先递归输出m除以10的结果，然后输出m除以10的余数
        f(m / 10);
        cout << " " << m % 10;
    }
}

int main() {
    int n;
    // 输入测试案例的数量n
    cin >> n;
    while (n--) {
        int m;
        // 输入每组案例的整数m
        cin >> m;
        // 调用函数f来输出整数m的每个数字
        f(m);
        // 每组案例输出后换行
        cout << endl;
    }
    return 0;
}
