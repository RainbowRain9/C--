/*
描述
统计字符串中每个字母字符出现的次数

输入
每组案例由一个字符串构成。

一个正整数n，表示有n组案例。

输出
针对每组案例，输出字符串中每个字母字符出现的次数。
要求：
1、对应的大小写字母视为同一个字母，比如A和a；
2、只要输出出现次数大于0的字母和出现次数；
3、输出时，字母用大写表示，按字母表中的顺序；
4、字母和出现次数之间有一个空格，每个字母的出现次数输出后换行。

样例输入
2

CCaAbbb

zzzzzz

样例输出
A 2

B 3

C 2

Z 6
*/

#include <iostream>
using namespace std;

int main()
{
    int n;    // 测试案例的数量
    cin >> n; // 读取测试案例的数量
    while (n--)
    {                        // 对于每个测试案例
        string s;            // 定义一个字符串变量s来存储输入的字符串
        cin >> s;            // 读取输入的字符串
        int count[26] = {0}; // 定义一个整数数组count，用于跟踪每个字母的出现次数

        // 遍历字符串中的每个字符
        for (char c : s)
        {
            // 手动将字符转换为大写并找到对应的字母表位置
            if (c >= 'a' && c <= 'z')
            {
                c -= ('a' - 'A'); // 将小写字母转换为大写字母
            }
            int index = c - 'A'; // 找到字母在字母表中的位置
            count[index]++;      // 增加对应字母的出现次数
        }

        // 按字母表顺序输出每个字母的出现次数
        for (int i = 0; i < 26; i++)
        {
            if (count[i] > 0)
            {                                  // 只输出出现次数大于0的字母
                cout << char(i + 'A') << " " << count[i] << endl; // 输出大写字母和出现次数
            }
        }
    }
    return 0; // 程序结束
}